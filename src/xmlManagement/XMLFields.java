package xmlManagement;

public class XMLFields {

	private final String nameFieldTitle = "simName";
	private final String titleFieldTitle = "title";
	private final String authorFieldTitle = "author";
	private final String heightFieldTitle = "gridHeight";
	private final String widthFieldTitle = "gridWidth";
	private final String stateFieldTitle = "numberOfStates";

	public String getNameFieldTitle() {
		return nameFieldTitle;
	}

	public String getTitleFieldTitle() {
		return titleFieldTitle;
	}

	public String getAuthorFieldTitle() {
		return authorFieldTitle;
	}

	public String getHeightFieldTitle() {
		return heightFieldTitle;
	}

	public String getWidthFieldTitle() {
		return widthFieldTitle;
	}

	public String getStateFieldTitle() {
		return stateFieldTitle;
	}

}
